
// Metodos de objetos

const producto = {
    nombre: " Monitor 20 pulgadas",
    precio: 300,
    disponible: true
}

console.log(Object.keys( producto ));   // retorna un arreglo con las keys del objeto   -   izquierda


console.log(Object.values( producto ));   // retorna un arreglo con los valores de las llaves   -   derecha


console.log(Object.entries( producto ));   // retorna un arreglo con todo   -   all
