
// Las Funciones permiten tener un codigo mas ordenado, ademas las funciones tambien son Reutilizables 

// Declaracion de funcion (funtion declaration)
function sumar() {
    console.log(2+2);
}

sumar();    // Llama la funcion sumar




// Expresion de funcion (funtion Expression)
const sumar2 = function () {
    console.log(3+3);
}

sumar2();



